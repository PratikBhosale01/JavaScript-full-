//operators:-
//Used to perform some operation on data
// + -  / *
// Modulus    "%"
// Exponentiation "**"
// Increment "++"
// Decrement "--"
let A = 7;
let B = 3;
let C = A - B;
let D = A + B;
let E = A * B;
let F = A / B;
let G = A % B;
let H = A ** B; //A^B 



console.log("A =", A, "& B =",B);
console.log("A + B = ", D ); 
console.log("A - B = ", C );
console.log("A * B = ", E );
console.log("A / B = ", F );
console.log("A % B = ", G );
console.log("A** B = ", H );
console.log("A++ =", A++ );   //post increment
console.log("B-- =", B-- );  // post decrement
console.log("++A =", ++A );  // pre increment
console.log("--B =", --B );  //pre decrement